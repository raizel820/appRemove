// GET /api/pdf-configuration - Get PDF configuration
export async function GET() {
  try {
    // Try to get any config first
    const config = await db.pDFConfiguration.findFirst();

    if (!config) {
      return NextResponse.json(null);
    }

    // Return full config object (all available fields)
    return NextResponse.json(config);
  } catch (error) {
    console.error("Error fetching PDF configuration:", error);
    return NextResponse.json(
      { error: "Failed to fetch PDF configuration" },
      { status: 500 }
    );
  }
}

